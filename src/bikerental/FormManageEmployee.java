    @Override
    public void renderTable() {
        List<Employee> employeeList = service.findAllEmployee();
        Iterator<Employee> cursor = employeeList.iterator();

        String[] column = {"ชื่อผู้ใช้งาน", "รหัสผ่าน", "สถานะ"};
        tableEmployee.getTableHeader().setFont(new Font("TH Sarabun New", Font.BOLD, 18));

        model = new DefaultTableModel(column, 0);

        try {
            while (cursor.hasNext()) {
                Employee employee = cursor.next();
                String employeeId = employee.getEmployeeId();
                String employeeRole = employee.getRole();
                String employeePassword = employee.getPassword();
                model.addRow(new Object[]{employeeId, employeePassword, employeeRole});
            }
        } finally {

        }
        tableEmployee.setModel(model);
    }

    public void search() {
        try {
            Employee cursor = service.findEmployeeById(txtSearch.getText());

            String[] column = {"ชื่อผู้ใช้งาน", "รหัสผ่าน", "สถานะ"};
        tableEmployee.getTableHeader().setFont(new Font("TH Sarabun New", Font.BOLD, 18));

            model = new DefaultTableModel(column, 0);
            Employee employee = cursor;
            String employeeId = employee.getEmployeeId();
            String employeeRole = employee.getRole();
            String employeePassword = employee.getPassword();
            model.addRow(new Object[]{employeeId, employeePassword, employeeRole});

            tableEmployee.setModel(model);
        } catch (Exception e) {
            alertMessage("ไม่มีรหัสรถคันนี้อยู่ในระบบ");
            renderTable();
            clear();
        }
    }

