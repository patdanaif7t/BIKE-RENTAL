    public boolean isFill(String field) {
        return !field.equals("");
    }

    public boolean isFieldAllFill() {
        return isFill(txtId.getText())
                && isFill(txtPassword.getText());
    }

    private boolean checkSelectedRow() {
        int rowIndex = tableEmployee.getSelectedRow();
        return rowIndex != -1;
    }

    private void add() {
        if (isFieldAllFill()) {
            String id = txtId.getText();
            String password = txtPassword.getText();
            String role = combo_level.getSelectedItem().toString();
            if(role.equals("ผู้ดูแลระบบ")){
                role = "Admin";
            } else if(role.equals("พนักงาน")) {
                role = "Employee";
            }
            Employee employee = new Employee(id, password, role);
            if (service.addEmployee(employee)) {
                alertMessage("เพิ่มข้อมูลพนักงานสำเร็จ");
                renderTable();
                clear();
            } else {
                alertMessage("มีชื่อผู้ใช้งานนี้อยู่แล้วในระบบ");
                clear();
            }
        } else {
            alertMessage("กรุณากรอกข้อมูลให้ครบถ้วน");
            clear();
        }
    }

    private void remove() {
        if (checkSelectedRow()) {
            int rowIndex = tableEmployee.getSelectedRow();
            String employeeId = model.getValueAt(rowIndex, 0).toString();
            if (service.removeEmployeeById(employeeId)) {
                alertMessage("ลบพนักงานสำเร็จ");
                renderTable();
                clear();
            } else {
                alertMessage("ผิดพลาด");
                clear();
            }
        } else {
            alertMessage("กรุณาเลือกพนักงานที่ต้องการลบในตาราง");
        }
    }

    public void changePassword() {
        if (isFieldAllFill()) {
            String employeeId = txtId.getText();
            String employeePassword = txtPassword.getText();
            Employee newEmployee = new Employee();
            newEmployee.setEmployeeId(employeeId);
            newEmployee.setPassword(employeePassword);
            if (service.findEmployeeById(employeeId) != null) {
                if (service.editEmployeeById(employeeId, newEmployee)) {
                    alertMessage("เปลี่ยนรหัสผ่านสำเร็จ");
                    renderTable();
                    clear();
                } else {
                    alertMessage("เกิดข้อผิดพลาด กรุณาลองใหม่อีกครั้ง!");
                    clear();
                }
            } else {
                alertMessage("ไม่พบรหัสผู้ใช้งานนี้อยู่ในระบบ");
            }
        } else {
            alertMessage("กรุณากรอกข้อมูลให้ครบถ้วน");
        }
    }

    public void clear() {
        txtId.setText("");
        txtPassword.setText("");
        txtSearch.setText("");
    }

    public void searchAll() {
        renderTable();
    }

