    public void returnBike() {
        int rowIndex = tableBikeRent.getSelectedRow();

        if (isFieldAllFill() && txtFine.getText().matches("[0-9]+")) {
            String invoiceId = model.getValueAt(rowIndex, 0).toString();
            String bikeId = model.getValueAt(rowIndex, 1).toString();
            String status = model.getValueAt(rowIndex, 5).toString();
            Float fine = Float.parseFloat(txtFine.getText());
            if (service.returnInvoice(invoiceId, bikeId, status, fine)) {
                alertMessage("คืนรถสำเร็จ");
                renderTable();
                clear();
            }
        } else {
            alertMessage("กรุณากรอกข้อมูลให้ครบถ้วน");
        }
    }

