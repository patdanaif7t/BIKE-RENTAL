    @Override
    public void renderTable() {
        List<Invoice> invoiceList = service.findAllInvoice();
        Iterator<Invoice> cursor = invoiceList.iterator();

        String[] column = {"รหัสการเช่า-คืน", "รหัสรถ", "วันที่เช่า", "วันที่คืน", "วันที่คืนจริง", "สถานะ"};
        tableBikeRent.getTableHeader().setFont(new Font("TH Sarabun New", Font.BOLD, 18));

        DefaultTableModel model = new DefaultTableModel(column, 0);

        try {
            while (cursor.hasNext()) {
                Invoice invoice = cursor.next();
                String InvoiceId = invoice.getInvoiceId();
                String BikeId = invoice.getBikeId();
                String DateRent = invoice.getDateRent();
                String DateReturn = invoice.getDateReturn();
                String DateReturnReal = invoice.getDateReturnReal();
                String Status = invoice.getStatus();
                model.addRow(new Object[]{InvoiceId, BikeId, DateRent, DateReturn, DateReturnReal, Status});
            }
        } finally {

        }
        tableBikeRent.setModel(model);
    }

    private void search() {
        if (isFill(txtSearch.getText())) {
            try {
                Invoice cursor = service.findInvoiceById(txtSearch.getText());

                String[] column = {"รหัสการเช่า-คืน", "รหัสรถ", "วันที่เช่า", "วันที่คืน", "วันที่คืนจริง", "สถานะ"};
                tableBikeRent.getTableHeader().setFont(new Font("TH Sarabun New", Font.BOLD, 18));

                model = new DefaultTableModel(column, 0);
                Invoice invoice = cursor;
                String InvoiceId = invoice.getInvoiceId();
                String BikeId = invoice.getBikeId();
                String DateRent = invoice.getDateRent();
                String DateReturn = invoice.getDateReturn();
                String DateReturnReal = invoice.getDateReturnReal();
                String Status = invoice.getStatus();
                model.addRow(new Object[]{InvoiceId, BikeId, DateRent, DateReturn, DateReturnReal, Status});

                tableBikeRent.setModel(model);
            } catch (Exception e) {
                alertMessage("ไม่พบรายการเช่า-คืนนี้ในระบบ");
                renderTable();
                txtSearch.setText("");
                clear();
            }
        } else {
            alertMessage("กรุณากรอกรหัสการเช่า-คืนรถ");
        }
    }

    private void searchAll() {
        renderTable();
        txtSearch.setText("");
    }

